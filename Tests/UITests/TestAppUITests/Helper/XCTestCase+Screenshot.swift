import XCTest


extension XCTestCase {
    func takeScreenshot() {
        let screenshot = XCUIScreen.main.screenshot()
        let fullScreenshotAttachment = XCTAttachment(screenshot: screenshot)
        fullScreenshotAttachment.lifetime = .keepAlways
        
        add(fullScreenshotAttachment)
    }
}
